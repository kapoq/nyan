module Nyan
    module Tail
    SHAPE_LIST =<<-EOF
..................
..................
..................
..................
..................
--
..................
..................
..................
..................
..................
--
..................
..................
..................
..................
..................
EOF

    def self.to_frames
      @frames ||= SHAPE_LIST.gsub(/\./, SPACE).gsub(/X/, BLOCK).split(/--./m)
    end
  end    
end
